// operating with variables
 
#include <iostream>
using namespace std;
int main()
{
// declaring variables:
int a=5;
 
char b='1';
 
float c=1.02;
 
double d=2.087;
 
bool e=6;
 
// shows size
cout << "size of integer " << sizeof(int) << " bytes "<<" a = "<<a<<endl;
cout << "size of char " << sizeof(char) << " bytes "<<" b = "<<b<<endl;
cout << "size of integer " << sizeof(float) << " bytes "<<" c = "<<c<<endl;
cout << "size of integer " << sizeof(double) << " bytes "<<" d = "<<d<<endl;
cout << "size of integer " << sizeof(bool) << " bytes "<<" e = "<<e<<endl;
return 0;
}


